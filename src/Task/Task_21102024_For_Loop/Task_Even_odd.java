package Task.Task_21102024_For_Loop;

public class Task_Even_odd {

    public static void main(String[] args) {

        for(int i=0; i<=50; i++){

            if(i%2==0){
                System.out.println("The number is Even "+i);
            }
            else{
                System.out.println("The number is Odd "+i);
            }
        }
    }
}
